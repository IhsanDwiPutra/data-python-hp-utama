# if dan else

nama = input("Siapa nama anda \n= ")

# 1. If inline
print("If inline")
if nama == "ihsan": print("kamu keren abiezzzzzzz!!!")
print(f"Terima kasih {nama}")
    
# 2. If indentation
print("\nIf indentation")
if nama == "ihsan":
    print("anda sangat hebat")
print(f"Terima kasih {nama}")
    
# 3. Else statement
print("\nElse statement")
if nama == "ihsan":
    print("kamu sangat mantapp sekaliii")
    
else:
    print("sayang sekali kamu bukan si dia :(")
    
print(f"Terima kasih {nama}")